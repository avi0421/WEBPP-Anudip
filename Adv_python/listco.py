# List comprehension for cubes
squares = [x**3 for x in range(10)]
print(squares)

# Square of i
for i in range(10):
    res = i * i
    print(res)

################################################
# List comprehension for squares
squares = [x**2 for x in range(15,26)]
print(squares)

# Square of i
for i in range(1,15):
    res = i * i
    print(res)

################################################
# List comprehension for squares
squares = [x**2 for x in range(1,25) if x%2==0]
print(squares)

# Square of i
for i in range(1,15):
    res = i * i
    print(res)

